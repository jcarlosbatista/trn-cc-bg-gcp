create table public.albums
(
	incr int GENERATED BY DEFAULT AS IDENTITY,
    artist_id bigint,
	album_title text,
	genre text,
	year_of_pub bigint,
	num_of_tracks bigint,
	num_of_sales bigint,
	rolling_stone_critic bigint,
	mtv_critic bigint,
	music_maniac_critic bigint,
	dt_current_timestamp timestamp,
	PRIMARY KEY (incr)
);